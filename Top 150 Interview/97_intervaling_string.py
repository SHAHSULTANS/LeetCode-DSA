class Solution:
    def isInterleave(self, s1: str, s2: str, s3: str) -> bool:
        l1=len(s1)
        l2=len(s2)
        if l1+l2==len(s3):
            
            dp=[[False]*(l2+1) for _ in range(l1+1)]
            
            
            self.flag=False
            
            def dfs(i,j,k):
                if dp[i][j]==True:
                    return
                
                if i==l1 and j==l2:
                    self.flag=True
                    return

                dp[i][j] = True    
                if i<l1:
                    if s1[i]==s3[k]:
                        dfs(i+1,j,k+1)
                
                if j<l2:
                    if s2[j]==s3[k]:
                        dfs(i,j+1,k+1)
              

            dfs(0,0,0)
            return self.flag
                        
                                
        else:
            return False
        
        

