class Solution:
    def exist(self, board: List[List[str]], word: str) -> bool:
        row=len(board)
        col=len(board[0])
        
        visited=[[False]*col for _ in range(row)]
        self.ans=False
        
        
        def dfs(i,j,ctn):
            if ctn==len(word):
                self.ans=True
                return
                
            # Left move
            if j-1 >= 0 and board[i][j-1] == word[ctn] and not visited[i][j-1]:
                visited[i][j-1] = True
                # ar.append(word[ctn])
                dfs( i, j-1, ctn + 1)
                visited[i][j-1] = False
                # ar.pop()
                
            #right move
            if j+1<col and board[i][j+1] == word[ctn] and not visited[i][j+1]:
                visited[i][j+1] = True
                # ar.append(word[ctn])
                dfs( i, j+1, ctn + 1)
                visited[i][j+1] = False
                # ar.pop()
            
            
            #up move
            
            if i-1>= 0 and board[i-1][j] == word[ctn] and not visited[i-1][j]:
                visited[i-1][j] = True
                # ar.append(word[ctn])
                dfs( i-1, j, ctn + 1)
                visited[i-1][j] = False
                # ar.pop()
                
            #down move
            if i+1<row and board[i+1][j] == word[ctn] and not visited[i+1][j]:
                visited[i+1][j] = True
                # ar.append(word[ctn])
                dfs( i+1, j, ctn + 1)
                visited[i+1][j] = False
                # ar.pop()
        
        
        for i in range(row):
            for j in range(col):
                if board[i][j]==word[0]:
                    visited[i][j]=True
                    dfs(i,j,1)
                    visited[i][j]=False
                    
                    
        return self.ans
        
        
        
